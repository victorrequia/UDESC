import math
import numpy as np

def best_reta(x,y):
    n = len(x)
    xises = 0
    yis = 0
    xises_quadrado = 0
    xises_yis = 0
    for i in range(n):
        xises += x[i]
        yis += y[i]
        xises_quadrado += x[i]**2
        xises_yis +=x[i] * y[i]
    A = [[n,xises],[xises,xises_quadrado]]
    B = [yis,xises_yis]
    a,b = np.linalg.solve(A,B)
    return a,b

